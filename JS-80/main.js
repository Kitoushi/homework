// 1) Четные числа от 2 до 12
console.log("Четные числа от 2 до 12 (используя цикл while):");
let j = 2;
while (j <= 12) {
  if (j % 2 === 0) {
    console.log(j);
  }
  j++;
}

// 2) Числа от 10 до 1 в обратном порядке
console.log("Числа от 10 до 1 в обратном порядке (используя цикл while):");
let k = 10;
while (k >= 1) {
  console.log(k);
  k--;
}